CREATE TABLE CIRCUIT_LOG_Z (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  CIRCUIT_NAME VARCHAR2(200),
  THEME VARCHAR2(100),
  COMPONENT_COUNT NUMBER,
  POWER_STATE VARCHAR2(10),
  LOG_TS TIMESTAMP
);

CREATE OR REPLACE PROCEDURE INSERT_CIRCUIT_LOG_Z (
  p_circuit_name IN VARCHAR2,
  p_theme IN VARCHAR2,
  p_components IN NUMBER,
  p_power_state IN VARCHAR2,
  p_ts IN TIMESTAMP
) AS
BEGIN
  INSERT INTO CIRCUIT_LOG_Z (CIRCUIT_NAME, THEME, COMPONENT_COUNT, POWER_STATE, LOG_TS)
  VALUES (p_circuit_name, p_theme, p_components, p_power_state, p_ts);
  COMMIT;
END INSERT_CIRCUIT_LOG_Z;
/

COMMIT;

